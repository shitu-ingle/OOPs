package inheritance;

public class Bike {
	int wheels;
	int seats;
	String fule;
	
	public Bike() {}

	public Bike(int wheels, int seats, String fule) {
		this.wheels = wheels;
		this.seats = seats;
		this.fule = fule;
	}
	public void displayBike()
    {
		System.out.println("Wheels :"+wheels);
		System.out.println("Seats :"+seats);
		System.out.println("Fule :"+fule);
		System.out.println("======================================");
    }
	

}
